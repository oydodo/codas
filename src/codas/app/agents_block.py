def _enforcement(severity: str | None, status: str | None) -> str:
    """Derive how the gate treats a policy, from its declared (severity, status):

      - ``planned``  — declared on the roadmap, NOT yet implemented (the gate never runs it).
      - ``gated``    — an error the gate blocks on (via ``check``, ``wiki --verify`` or the
                       byte-identical inventory).
      - ``advisory`` — a warning the gate SURFACES but does not block on.

    Purely a projection of the two declared fields, so the agent learns exactly which failure
    classes the gate is silent on without a second hand-maintained classification.
    """
    if status == "planned":
        return "planned"
    if severity == "error":
        return "gated"
    return "advisory"


def _blurb(policy_id: str) -> str:
    try:
        return _POLICY_BLURB[policy_id]
    except KeyError as error:  # pragma: no cover - exercised via render on a new policy
        raise ValueError(
            f"policy {policy_id!r} is declared in policies.yml but has no AGENTS blurb in "
            "agents_block._POLICY_BLURB; add a one-line 'what it catches' (no slash+extension "
            "path spans) so the governance block stays in sync with the policy registry"
        ) from error


def render_codas_block(policies_raw: dict, structure_map: StructureMap) -> str:
    """Render the Codas governance block (between the markers) as a PURE, deterministic
    projection of the policy registry + Structure Map. Sorted, no timestamp, byte-identical
    for identical inputs.

    must-hold #4: extension-bearing paths (unit paths like ``.codas/structure.yml``) are
    rendered as PLAIN table cells — never inside backticks or markdown links — so the scanned
    AGENTS.md gains no new ``doc_claim`` and the block is inventory-hash-neutral. Backticks are
    used only on claim-safe tokens (policy ids, unit ids, extension-less command names).
    """
    declared = policies_raw.get("policies", {}) or {}
    policy_rows = sorted(
        (
            (
                pid,
                (body or {}).get("severity"),
                _enforcement((body or {}).get("severity"), (body or {}).get("status")),
                _blurb(pid),
            )
            for pid, body in declared.items()
        ),
        key=lambda row: row[0],
    )

    lines = [
        BLOCK_START,
        _GENERATED_NOTE,
        "",
        "# Codas governance",
        "",
        "This repository is governed by Codas (Code Atlas System). Before changing code, read",
        "this map and query the live atlas — do NOT trust your inferred structure over the",
        "verified facts. This block is regenerated from the policy registry and Structure Map;",
        "edits inside the markers are overwritten.",
        "",
        "## Policy catalog",
        "",
        "How the gate treats each policy: **gated** = an error it blocks on; **advisory** = a",
        "warning it surfaces; **planned** = declared but not yet enforced.",
        "",
        "| policy | severity | enforcement | what it catches |",
        "| --- | --- | --- | --- |",
    ]
    for pid, severity, enforcement, blurb in policy_rows:
        lines.append(
            "| `{}` | {} | {} | {} |".format(
                guard_table_cell(pid),
                guard_table_cell(severity or "—"),
                enforcement,
                guard_table_cell(blurb),
            )
        )

    lines += [
        "",
        "## Ungateable failure classes (the gate is SILENT here)",
        "",
        "The gate formalizes only a subset of correctness. These design-quality failures are",
        "caught by NO active policy today — the ownership map below and a reuse query are your",
        "only defense, so verify them yourself before writing:",
        "",
    ]
    lines += [f"- {_ungateable}" for _ungateable in _UNGATEABLE_CLASSES]

    lines += [
        "",
        "## Ownership & placement map",
        "",
        "Every governed path maps to a Structure-Map unit and owner. A changed file must map to",
        "a unit (missing_structure_owner); WHERE within an owned area a change belongs is the",
        "canonical-placement rule the gate cannot enforce — read it before adding files.",
        "",
        "| unit | path | owner | canonical placement |",
        "| --- | --- | --- | --- |",
    ]
    for unit in sorted(structure_map.units, key=lambda u: u.id):
        lines.append(
            "| `{}` | {} | {} | {} |".format(
                guard_table_cell(unit.id),
                guard_table_cell(unit.path),  # PLAIN — extension-bearing paths must not be claims
                guard_table_cell(unit.owner),
                guard_table_cell(unit.canonical_placement),
            )
        )

    lines += [
        "",
        "## Lookup protocol",
        "",
        "The live atlas is fresh and deterministic. Query it (the rendered `wiki/` book is for",
        "humans; these commands are the agent's table):",
        "",
        "- `codas preflight --task <id>` — read-first context, reuse candidates and affected units.",
        "- `codas query <kind>` — one fact block (symbols, imports, calls, units, …); add",
        "  `--select FIELD=VALUE` to filter.",
        "- `codas impact <symbol-or-path>` — what transitively calls a symbol or file.",
        "- `codas schema` — the queryable fact and field shape.",
        "- `codas check .` — the gate; run it before you commit.",
        "",
        BLOCK_END,
    ]
    return "\n".join(lines)


def splice_managed_block(existing: str, block: str) -> str:
    """Splice ``block`` (which itself includes the START/END markers) into ``existing``,
    preserving every byte OUTSIDE the markers. If the markers are present the region between
    them (inclusive) is replaced; otherwise the block is appended after the existing content.
    Deterministic and idempotent: re-splicing identical inputs reproduces identical bytes."""
    if BLOCK_START in existing and BLOCK_END in existing:
        pre, _, rest = existing.partition(BLOCK_START)
        _, _, post = rest.partition(BLOCK_END)
        return pre + block + post
    base = existing.rstrip("\n")
    if base:
        return f"{base}\n\n{block}\n"
    return f"{block}\n"


def agents_pages(repo: Path) -> dict[Path, str]:
    """The deterministic ``{AGENTS.md path: full spliced content}`` shared by write/verify.

    Reads the live policy registry + Structure Map, renders the Codas block, and splices it
    into the EXISTING AGENTS.md (preserving the Trellis block + hand content). A missing
    AGENTS.md yields a new file holding only the Codas block.
    """
    policies_raw = load_policies(repo / ".codas" / "policies.yml")
    structure_map = load_structure_map(repo / ".codas" / "structure.yml")
    block = render_codas_block(policies_raw, structure_map)
    agents_path = repo / "AGENTS.md"
    existing = (
        agents_path.read_text(encoding="utf-8") if agents_path.is_file() else ""
    )
    return {agents_path: splice_managed_block(existing, block)}


def write_agents_block(repo: Path) -> list[Path]:
    """Render and write the AGENTS.md Codas block; return the written paths (sorted).
    write_bytes pins UTF-8 + LF (text mode would translate LF to os.linesep on Windows,
    breaking byte-identical)."""
    written: list[Path] = []
    for path, content in agents_pages(repo).items():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_bytes(content.encode("utf-8"))
        written.append(path)
    return sorted(written)


def verify_agents_block(repo: Path) -> list[Path]:
    """AGENTS.md whose on-disk bytes differ from a fresh splice (the Codas block is stale,
    hand-edited, or missing); empty == up to date. Byte-compare IS the verifier — the block
    carries no machine-checked claims, freshness rides in the rendered bytes. Edits OUTSIDE the
    markers never flag (the expected content is the fresh block spliced over the current file)."""
    stale: list[Path] = []
    for path, content in agents_pages(repo).items():
        if not path.is_file() or path.read_bytes() != content.encode("utf-8"):
            stale.append(path)
    return sorted(stale)
