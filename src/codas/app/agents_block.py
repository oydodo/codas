# The Codas-managed region of AGENTS.md. Everything OUTSIDE these markers (the Trellis
# block, hand-authored sections) is preserved verbatim on every write; only the bytes
# BETWEEN them are a deterministic projection of `.codas/policies.yml` + `.codas/structure.yml`.
# Mirrors the Trellis block convention already in AGENTS.md so the two managed regions coexist.
BLOCK_START = "<!-- CODAS:START -->"
BLOCK_END = "<!-- CODAS:END -->"

_GENERATED_NOTE = (
    "<!-- GENERATED by `codas agents --write` from .codas/policies.yml + "
    ".codas/structure.yml. Do not hand-edit; regenerate to refresh. -->"
)

